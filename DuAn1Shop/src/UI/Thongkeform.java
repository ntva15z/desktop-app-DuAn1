    void fillCBXnamBH() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxNamBH.getModel();
        model.removeAllElements();

        List<Object> list = tkdao.getNamHD();
        for (Object object : list) {
            model.addElement(object.toString());
        }
        cbxNamBH.setSelectedItem(0);
    }

    void loadtblbanhangTK() {

        DefaultTableModel model = (DefaultTableModel) tblThongkeBanhang.getModel();
        model.setRowCount(0);
        int nam = Integer.valueOf(cbxNamBH.getSelectedItem().toString());
        List<Object[]> list = tkdao.getTThoadon(nam);
        for (Object[] object : list) {
            model.addRow(object);
        }
    }

    void loadtblBanhHangChiTietTK(int year, int month) {
        DefaultTableModel model = (DefaultTableModel) tblThongkebanhangchitiet.getModel();
        model.setRowCount(0);

        List<Object[]> list = tkdao.getTThoadonChiiet(year, month);
        for (Object[] object : list) {
            model.addRow(object);
        }
    }

