    KhachHangDAO khdao = new KhachHangDAO();

    void loadDBFromKH() {
        DefaultTableModel model = (DefaultTableModel) TblKhachHang.getModel();
        model.setRowCount(0);
        try {
            String tenKh = txtTimKiemKH.getText();
            List<KhachHang> list = khdao.selectByKeyword(tenKh);

            for (KhachHang kh : list) {

                Object[] row = {
                    kh.getMaKH(), kh.getTenKH(), kh.isGioitinh() ? "Nam" : "Nữ", kh.getDiachi(),
                    kh.getSdt(), kh.getEmail(), util.getRank(kh.getLoai()), kh.getGhichu()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    void insertDBFromKH() {
        if(!util.checkMaKH(txtMaKH, this)){
            DialogHelper.alert(this, "Khách hàng đã tồn tại");
            return;
        }
         if(!util.checkNull(txtMaKH,"Mã khách hàng", this)){
            return;
        }
        if(!check()){
            return;
        }
        KhachHang model = getModelKH();
        try {
            khdao.insert(model);
            this.loadDBFromKH();
            this.clearModelKH();
            DialogHelper.alert(this, "Thêm mới thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại" + e);
        }
    }

    void updateDBFromKH() {
        if(!check()){
            return;
        }
        KhachHang model = getModelKH();
        try {
            khdao.update(model);
            this.loadDBFromKH();
            this.clearModelKH();
            DialogHelper.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại");
        }
    }

    void deleteDBFromKH() {
         if(!util.checkLicense(this)){
           return;
       }

        if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa Khách hàng này?")) {
            String makh = txtMaKH.getText();
            try {
                khdao.delete(makh);
                this.loadDBFromKH();
                this.clearModelKH();
                DialogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
    }

    void editDisplayKH() {
        try {
            String makh = (String) TblKhachHang.getValueAt(this.indexKH, 0);
            KhachHang model = khdao.findById(makh);
            if (model != null) {
                this.setModelKH(model);
                this.setStatusFormKH(false);
                TblKhachHang.setRowSelectionInterval(indexKH, indexKH);
            }
            if(!ShareHelper.USER.isVaitro()){
            btnXoaKh.setEnabled(false);
        }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void clearModelKH() {
        this.setModelKH(new KhachHang());
        this.setStatusFormKH(true);
        if(!ShareHelper.USER.isVaitro()){
            btnXoaKh.setEnabled(false);
        }
    }

    void setModelKH(KhachHang model) {
        txtMaKH.setText(model.getMaKH());
        txtTenKH.setText(model.getTenKH());
        rdoNamKH.setSelected(model.isGioitinh());
        rdoNuKH.setSelected(!model.isGioitinh());
        txtDiaChiKH.setText(model.getDiachi());
        txtSDTKH.setText(model.getSdt());
        txtEmailKH.setText(model.getEmail());
        txtLoai.setText(util.getRank(model.getLoai()));
        txtGhiChuKH.setText(model.getGhichu());
    }

    KhachHang getModelKH() {
        KhachHang model = new KhachHang();
        model.setMaKH(txtMaKH.getText());
        model.setTenKH(txtTenKH.getText());
        model.setGioitinh(rdoNamKH.isSelected());
        model.setDiachi(txtDiaChiKH.getText());
        model.setSdt(txtSDTKH.getText());
        model.setEmail(txtEmailKH.getText());
        model.setLoai(util.setRank(txtLoai.getText()));
        model.setGhichu(txtGhiChuKH.getText());
        return model;
    }

    void setStatusFormKH(boolean insertable) {
        txtMaKH.setEditable(insertable);
        btnThemKH.setEnabled(insertable);
        btnCapNhatKH.setEnabled(!insertable);
        btnXoaKh.setEnabled(!insertable);

        boolean first = this.indexKH > 0;
        boolean last = this.indexKH < TblKhachHang.getRowCount() - 1;
        btnFirstKH.setEnabled(!insertable && first);
        btnPrevKH.setEnabled(!insertable && first);
        btnNextKH.setEnabled(!insertable && last);
        btnLastKH.setEnabled(!insertable && last);
    }
