    UtilityHelper util = new UtilityHelper();

    void insertSPPN() {
        if (!util.checkInt(txtSLSPPN, this, "Số lượng sản phẩm")) {
            return;
        }
        DialogHelper.alert(this, "Thêm thành công");
        PhieuNhapCT model = getModelSPPN();
        for (PhieuNhapCT pnct : listPN) {
            if (model.getMaSP().equals(pnct.getMaSP())) {
                DialogHelper.alert(this, "Sản phẩm đã có trong phiếu");
                return;
            }
        }
        listPN.add(model);
        loadTblSanPhamPN();
        TongtienPN();
    }

    void deleteSPPN() {
        if(tblPhieuNhapCT.getSelectedRow() != -1){
            listPN.remove(tblPhieuNhapCT.getSelectedRow());
        }else{
            DialogHelper.alert(this, "Bạn chưa chọn sản phẩm");
        }

        loadTblSanPhamPN();
        TongtienPN();

    }

    void updateSPPN() {
          if (!util.checkInt(txtSLSPPN, this, "Số lượng sản phẩm")) {
            return;
        }
        PhieuNhapCT model = getModelSPPN();
        if (tblPhieuNhapCT.getSelectedRow() >= 0) {
            int i = tblPhieuNhapCT.getSelectedRow();
            listPN.set(i, model);

            loadTblSanPhamPN();
            TongtienPN();
        }
    }

    void insertPhieuNhap() {
        
        if(listPN.isEmpty()){
            DialogHelper.alert(this, "Bạn chưa thêm sản phẩm trên phiếu ");
            return;
        }
        
        PhieuNhap modelpn = this.getModelPN();
        try {
            pndao.InsertPhieuNhap(modelpn);
            for (PhieuNhapCT model : listPN) {
                pnctdao.InsertPhieuNhapCT(model);
                SanPham sp = spdao.findById(model.getMaSP());
                sp.setSoLuong(sp.getSoLuong() + model.getSL());
                spdao.update(sp);

            }
            DialogHelper.alert(this, "Lưu Phiếu thành công");
            loadDBFormPhieuNhap();
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi thêm Phiếu nhập");
        }
    }

    void TongtienPN() {
        double sum = 0;
        for (int i = 0; i < tblPhieuNhapCT.getRowCount(); i++) {

            sum = sum + (double) tblPhieuNhapCT.getValueAt(i, 4);
        }
        txtTongtien.setText(String.valueOf(sum));
    }

    void sinhMaPN() {
        String mapn = "PN";
        String ngay = txtNgayNhapPN.getText().replace("/", "");
        String stt = String.valueOf(tblPhieuNhap.getRowCount() + 1);
        txtMaPN.setText(mapn + ngay + stt);
    }

    void rpPhieuNhap() {
        if(!util.checkMaPN(txtMaPN)){
            if(DialogHelper.confirm(this, "Phiếu nhập chưa được tạo bạn có muốn tạo phiếu nhập này")){
                insertPhieuNhap(); 
                return;
            }else{
                return;
            }
        }
        PhieuNhap model = this.getModelPN();
        HashMap<String, Object> hs = new HashMap<>();;
        hs.put("maphieu", model.getMaPN());
        hs.put("tennv", model.getMaNVPN());
        hs.put("ngaynhap", DateHelper.toString(model.getNgayNhap()));
        hs.put("ghichu", model.getGhiChu());
        hs.put("totalmoney", txtTongtien.getText());

        DefaultTableModel tblmodel = (DefaultTableModel) tblPhieuNhapCT.getModel();

        try {
            JasperDesign jd = JRXmlLoader.load("src\\report\\rpPhieuNhap.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(jd);

            JasperPrint jp = JasperFillManager.fillReport(jr, hs, new JRTableModelDataSource(tblmodel));
            JasperViewer.viewReport(jp, true);

        } catch (JRException ex) {
            ex.printStackTrace();
        }

    }

