    void init() {
        fillCBXnamPN();
        loadtblPhieunhapTK();
        fillCBXnamBH();
        loadtblbanhangTK();
    }
    ThongKeDAO tkdao = new ThongKeDAO();

    void fillCBXnamPN() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxNamPN.getModel();
        model.removeAllElements();

        List<Object> list = tkdao.getNamPN();
        for (Object object : list) {
            model.addElement(object.toString());
        }
        cbxNamPN.setSelectedItem(0);
    }

    void loadtblPhieunhapTK() {

        DefaultTableModel model = (DefaultTableModel) tblThongkePhieunhap.getModel();
        model.setRowCount(0);
        int nam = Integer.valueOf(cbxNamPN.getSelectedItem().toString());
        List<Object[]> list = tkdao.getTTphieunhap(nam);
        for (Object[] object : list) {
            model.addRow(object);
        }
    }

    void loadtblPhieuNhapChiTietTK(int year, int month) {
        DefaultTableModel model = (DefaultTableModel) tblThongkephieunhapct.getModel();
        model.setRowCount(0);

        List<Object[]> list = tkdao.getTTphieunhapChiiet(year, month);
        for (Object[] object : list) {
            model.addRow(object);
        }

    }
