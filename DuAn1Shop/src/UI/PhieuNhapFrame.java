    void initPN() {
        loadDBFormPhieuNhap();
        loadCboSanPhamPN();
        txtMaNVPN.setText(ShareHelper.USER.getMaNV());
        txtNgayNhapPN.setText(DateHelper.toString(new Date()));
        cboSanPhamPN.setSelectedIndex(0);
        this.sinhMaPN();
    }

    void loadDBFormPhieuNhap() {
        DefaultTableModel model = (DefaultTableModel) tblPhieuNhap.getModel();
        model.setRowCount(0);
        try {
            List<PhieuNhap> listPN = pndao.selectByKeyword(txtTimKiemPN.getText());
            for (PhieuNhap pn : listPN) {
                Object[] obj = new Object[]{pn.getMaPN(), pn.getMaNVPN(), pn.getNgayNhap(), pn.getGhiChu()};
                model.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu phiếu nhập");
        }

    }

    void loadCboSanPhamPN() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboSanPhamPN.getModel();
        model.removeAllElements();
        try {
            List<SanPham> listsp = spdao.selectByKeyword(txtTimKiemSPPN.getText());
            listsp.forEach((sp) -> {
                model.addElement(sp);
            });
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu sản phẩm");
        }

    }

    void loadTblSanPhamPN() {
        DefaultTableModel model = (DefaultTableModel) tblPhieuNhapCT.getModel();
        model.setRowCount(0);
        try {

            for (PhieuNhapCT pn : listPN) {
                SanPham sp = spdao.findById(pn.getMaSP());
                Double thanhtien = pn.getSL() * pn.getDongia();
                Object[] obj = new Object[]{pn.getMaSP(), sp.getTenSP(), pn.getSL(), pn.getDongia(), thanhtien};
                model.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu phiếu nhập");
        }

    }

    void editTBLPhieuNhap() {
        try {
            String mapn = (String) tblPhieuNhap.getValueAt(this.indexPN, 0);
            PhieuNhap modelPN = pndao.findById(mapn);
            if (modelPN != null) {
                this.setModelPN(modelPN);
                this.setStatusFormPN(false);

            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

