    boolean check(){
        if(!util.checkNull(txtTenKH, "Tên Khách Hàng", this)){
            return false;
        }
       
        if(!util.checkName(txtTenKH, this)){
            return false;
        }
         if(!util.checkNull(txtSDTKH, "SDT Khách Hàng", this)){
            return false;
        }
         
          if(!util.checkPhone(txtSDTKH, this)){
            return false;
        }
          
           if(!util.checkNull(txtEmailKH, "Email Khách Hàng", this)){
            return false;
        }
            if(!util.checkEmail(txtEmailKH, this)){
            return false;
        }
             if(!util.checkNull(txtDiaChiKH, "Địa chỉ Khách Hàng", this)){
            return false;
        }
              if(!util.checkNull(txtDiaChiKH,"Địa chỉ", this)){
            return false;
        }
         return true;
    }

